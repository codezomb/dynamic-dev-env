#!/bin/bash

set -e

# -----------------------------------------------------------------------------------------------------------------------------------------------------------------
# Rubygems
# -----------------------------------------------------------------------------------------------------------------------------------------------------------------

echo 'eval "$(rbenv init -)"' >> $HOME/.bashrc
eval "$(rbenv init -)"
rbenv global 3.1
gem install tmuxinator --user-install

# -----------------------------------------------------------------------------------------------------------------------------------------------------------------
# NodeJS
# -----------------------------------------------------------------------------------------------------------------------------------------------------------------

curl -o- https://raw.githubusercontent.com/nvm-sh/nvm/v0.39.7/install.sh | bash
export NVM_DIR=$HOME/.nvm;
source $NVM_DIR/nvm.sh;
nvm install v16
